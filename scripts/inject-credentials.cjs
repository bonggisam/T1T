#!/usr/bin/env node
/**
 * 빌드 타임 자격증명 주입.
 * .env 파일을 읽어 src/main/credentials.gen.ts 를 생성.
 * 생성된 파일은 gitignored — 빌드 결과(dist/)에 값이 inlined되어 배포.
 */
const fs = require('fs');
const path = require('path');
const dotenv = require('dotenv');

const projectRoot = path.join(__dirname, '..');
const envPath = path.join(projectRoot, '.env');
const outPath = path.join(projectRoot, 'src', 'main', 'credentials.gen.ts');

let env = {};
if (fs.existsSync(envPath)) {
  env = dotenv.parse(fs.readFileSync(envPath, 'utf-8'));
  console.log('[InjectCredentials] Loaded .env');
} else {
  console.warn('[InjectCredentials] .env not found — credentials will be empty (Google 연동 비활성)');
}

const content = `// AUTO-GENERATED by scripts/inject-credentials.cjs — DO NOT EDIT
// Built at: ${new Date().toISOString()}
export const GOOGLE_CLIENT_ID: string = ${JSON.stringify(env.GOOGLE_CLIENT_ID || '')};
export const GOOGLE_CLIENT_SECRET: string = ${JSON.stringify(env.GOOGLE_CLIENT_SECRET || '')};
`;

fs.writeFileSync(outPath, content);
console.log(`[InjectCredentials] Generated ${path.relative(projectRoot, outPath)}`);
